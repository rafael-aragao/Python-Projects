num1 = 20
num2 = 40

'''Adição'''
soma = num1 + num2

'''Substração'''

sub = num1 - num2

'''Multiplicação'''

mult = num1 * num2

'''Divisão'''

div = num1 / num2

'''Divisão Inteira'''
divInt = num1 // num2

'''Módulo'''

modulo = num1 % num2

'''Exponenciação'''

exp = num1 ** num2

print(f"Os diferentes calculos com duas variaveis a soma é {soma}, a subtração é {sub}")
print(f", a multiplicação é {mult}, a divisão é {div}, a divisão inteira é {divInt}")
print(f", o módulo é {modulo} e a exponenciação é {exp}")