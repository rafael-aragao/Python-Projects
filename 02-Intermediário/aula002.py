"""Operadores aritméticos + adição, - subtração * multiplicação \ divisão"""

num1 = 2
num2 = 7

resultado = num1 + num2

print(f"O resultado da adição é: {resultado}")

sub = num1 - num2

print(f"O resultado da subtração é: {sub}")

abacaxi = num1 * num2

print(f"O resultado da multiplicação é: {abacaxi}")

div = num1 / num2

print(f"O resultado da divisão é: {div}")